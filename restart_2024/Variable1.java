package restart_2024;

public class Variable1 {

	public static void main(String[] args) {
		
		int a = 5; //local variable
		System.out.println(a); //5
		
		int b = 100; //int type local variable
		System.out.println(b);
		b=200; //int type local var
		System.out.println(b);
		
		char c = 'K'; //char type local variable, should placed inside the single quote ''
		System.out.println(c);
		
		boolean d = false;
		System.out.println(d);
		
		System.out.println(3<4);
		
		int e = 100; //int type local variable
		int f = 200; //int type local var
		
		boolean z= e>f;
		System.out.println(z);
		

	}

}

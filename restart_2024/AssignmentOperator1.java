package restart_2024;

public class AssignmentOperator1 {

	public static void main(String[] args) {
		int a= 10;
		
		a += 5; //do not pass space b/w + and =
		System.out.println(a); //5
		
		a -= 7;
		System.out.println(a); //3
		
		a *= 6;
		System.out.println(a); //60
		
		a /= 2 ;
		
		System.out.println(a); //5
	}
}
